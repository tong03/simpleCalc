function addNum(a,b){
    return a + b;
}

function subtractNum(a,b){
    return a - b;
}

function multiplyNum(a,b){
    return a * b;
}

function divideNum(a,b){
    return a/b;
}

console.log(addNum(2,7));
console.log(subtractNum(10,7));
console.log(multiplyNum(2,7));
console.log(divideNum(24,3));